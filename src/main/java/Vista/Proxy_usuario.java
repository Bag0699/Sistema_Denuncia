    private void BTN_REGISTRARActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_BTN_REGISTRARActionPerformed
         UsuarioControlador registrar = new UsuarioControlador();
        String idUsuario = TXT_ID.getText();
        String nombre = TXT_NOMBRE.getText();
        String apellido = TXT_APELLIDO.getText();
        String email = TXT_EMAIL.getText();
        String telefono = TXT_TELEFONO.getText();
        String direccion = TXT_DIRECCION.getText();
        String tipoUsuario = TXT_TIPO.getText();
        Usuario USER = new Usuario(idUsuario, nombre, apellido, email, telefono, direccion, tipoUsuario);
        try {
        registrar.crearUsuario(USER);
        JOptionPane.showMessageDialog(this, "Usuario registrado con éxito");
    } catch (SQLException e) {
        e.printStackTrace();
        JOptionPane.showMessageDialog(this, "Error al registrar el usuario");
    }
        
    }//GEN-LAST:event_BTN_REGISTRARActionPerformed

