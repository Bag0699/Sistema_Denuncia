    private void BTN_CONSULTARActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_BTN_CONSULTARActionPerformed
         UsuarioControlador controlador = new UsuarioControlador();
    
    // Obtener el idUsuario del JTextField
    String idUsuario = TXT_ID.getText();
    
    try {
        // Llamar al método leerUsuario y obtener el objeto Usuario
        Usuario usuario = controlador.leerUsuario(idUsuario);
        
        // Verificar si el usuario fue encontrado
        if (usuario != null) {
            // Mostrar los datos del usuario en los JTextFields
            TXT_NOMBRE.setText(usuario.getNombre());
            TXT_APELLIDO.setText(usuario.getApellido());
            TXT_EMAIL.setText(usuario.getEmail());
            TXT_TELEFONO.setText(usuario.getTelefono());
            TXT_DIRECCION.setText(usuario.getDireccion());
            TXT_TIPO.setText(usuario.getTipoUsuario());
            JOptionPane.showMessageDialog(this, "Usuario encontrado y datos cargados");
        } else {
            JOptionPane.showMessageDialog(this, "Usuario no encontrado");
        }
    } catch (SQLException e) {
        e.printStackTrace();
        JOptionPane.showMessageDialog(this, "Error al leer el usuario");
    }
    }//GEN-LAST:event_BTN_CONSULTARActionPerformed

