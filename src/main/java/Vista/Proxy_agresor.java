    private void btn_consultarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_consultarActionPerformed

String idAgresor = TXT_ID.getText();

try {
   
    Agresor agresor = controlador.leerAgresor(idAgresor);
    
    if (agresor != null) {
     
        TXT_NOMBRE.setText(agresor.getNombre());
        TXT_APELLIDO.setText(agresor.getApellido());
        TXT_TELEFONO.setText(agresor.getTelefono());
        TXT_DIRECCION.setText(agresor.getDireccion());
        JOptionPane.showMessageDialog(this, "Agresor encontrado y datos cargados");
    } else {
        JOptionPane.showMessageDialog(this, "Agresor no encontrado");
    }
} catch (SQLException e) {
    e.printStackTrace();
    JOptionPane.showMessageDialog(this, "Error al leer el agresor");
}
    }//GEN-LAST:event_btn_consultarActionPerformed

    private void btn_DeshacerActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_DeshacerActionPerformed
      
    try {
        String idActual = TXT_ID.getText();
        Agresor agresor = controlador.leerAgresor(idActual);
        
        if (agresor != null) {
            
            controlador.deshacerActualizacion(agresor);
            
            agresor = controlador.leerAgresor(idActual);
            
            TXT_ID.setText(agresor.getIdAgresor());
            TXT_NOMBRE.setText(agresor.getNombre());
            TXT_APELLIDO.setText(agresor.getApellido());
            TXT_TELEFONO.setText(agresor.getTelefono());
            TXT_DIRECCION.setText(agresor.getDireccion());
            
            JOptionPane.showMessageDialog(this, "Cambios deshechos exitosamente");
        } else {
            JOptionPane.showMessageDialog(this, "No se pudo encontrar el agresor");
        }
    } catch (SQLException e) {
        e.printStackTrace();
        JOptionPane.showMessageDialog(this, "Error al deshacer los cambios");
    }


    }//GEN-LAST:event_btn_DeshacerActionPerformed

