String nombre = TXT_NOMBRE.getText();
String apellido = TXT_APELLIDO.getText();
String telefono = TXT_TELEFONO.getText();
String direccion = TXT_DIRECCION.getText();
Agresor agresor = new Agresor(idAgresor, nombre, apellido, telefono, direccion);
try {
    controlador.crearAgresor(agresor);
    JOptionPane.showMessageDialog(this, "Agresor registrado con √©xito");
} catch (SQLException e) {
    e.printStackTrace();
    JOptionPane.showMessageDialog(this, "Error al registrar el agresor");
}

