    private void BTN_REGISTRARActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_BTN_REGISTRARActionPerformed
      Denuncia denuncia = new Denuncia();
    denuncia.setIdDenuncia(TXT_IDD.getText());
    denuncia.setFec(TXT_FECHA.getText());
    denuncia.setDesc(TXT_DESCRIPCION.getText());
    denuncia.setEstado(TXT_ESTADO.getText());
    denuncia.setTipo(TXT_TIPO.getText());
    denuncia.setRelDenc(TXT_RELACION.getText());
    denuncia.setId_usuario(TXT_IDU.getText());
    denuncia.setId_agresor(TXT_IDA.getText());

    // Llamar al método crearDenuncia del controlador
    DenunciaControlador controlador = new DenunciaControlador();
    try {
        controlador.crearDenuncia(denuncia);
        JOptionPane.showMessageDialog(this, "Denuncia registrada exitosamente.");
    } catch (SQLException e) {
        JOptionPane.showMessageDialog(this, "Error al registrar la denuncia: " + e.getMessage(), "Error", JOptionPane.ERROR_MESSAGE);
    }
    }//GEN-LAST:event_BTN_REGISTRARActionPerformed

