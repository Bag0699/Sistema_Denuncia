    private void BTN_CONSULTARActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_BTN_CONSULTARActionPerformed
    String idDenuncia = TXT_IDD.getText();

    // Llamar al método leerDenuncia del controlador
    DenunciaControlador controlador = new DenunciaControlador();
    try {
        Denuncia denuncia = controlador.leerDenuncia(idDenuncia);
        if (denuncia != null) {
            // Rellenar los campos del formulario con los datos de la denuncia
            TXT_FECHA.setText(denuncia.getFec());
            TXT_DESCRIPCION.setText(denuncia.getDesc());
            TXT_ESTADO.setText(denuncia.getEstado());
            TXT_TIPO.setText(denuncia.getTipo());
            TXT_RELACION.setText(denuncia.getRelDenc());
            TXT_IDU.setText(denuncia.getId_usuario());
            TXT_IDA.setText(denuncia.getId_agresor());
            JOptionPane.showMessageDialog(this, "Denuncia encontrada y datos cargados.");
        } else {
            JOptionPane.showMessageDialog(this, "No se encontró ninguna denuncia con el ID proporcionado.", "Información", JOptionPane.INFORMATION_MESSAGE);
        }
    } catch (SQLException e) {
        JOptionPane.showMessageDialog(this, "Error al consultar la denuncia: " + e.getMessage(), "Error", JOptionPane.ERROR_MESSAGE);
    }
    }//GEN-LAST:event_BTN_CONSULTARActionPerformed

